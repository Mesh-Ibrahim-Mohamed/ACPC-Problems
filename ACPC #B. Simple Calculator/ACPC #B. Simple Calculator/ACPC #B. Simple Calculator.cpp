#include <iostream>
using namespace std;

int main()
{
	long long X, Y;

	
	

	cin >> X >> Y;

	long long sum = X + Y;
	long long multi = X * Y;
	long long Miuns = X - Y;

	cout << X << " + " << Y << " = " << sum << endl;

	cout << X << " * " << Y << " = " << multi << endl;

	cout << X << " - " << Y << " = " << Miuns << endl;

	return 0;

}